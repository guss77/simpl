$SIMPL_HOME/php/src/SIMPL.php is a implementation of the tclSurrogate
protocol in PHP.

Users should copy this SIMPL.php into their working PHP directory and add
a line 

require_once "SIMPL.php"

to their PHP scripts.

There is a sample which uses this library to implement a Sudoku puzzle
solver client at:

$SIMPL_HOME/php/sample/sudoku.php
